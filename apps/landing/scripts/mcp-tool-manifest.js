import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const landingRoot = resolve(__dirname, "..");
const mcpToolsRoot = resolve(landingRoot, "../mcp/src/tools");
const mcpToolsIndexPath = resolve(mcpToolsRoot, "index.ts");
export const generatedMcpToolManifestPath = resolve(
  landingRoot,
  "app/lib/mcp-tool-manifest.generated.ts",
);

function normalizeWhitespace(value) {
  return value.replace(/\s+/g, " ").trim();
}

function extractImportPathByRegisterFunction(indexSource) {
  const importPathByRegisterFunction = new Map();
  const importPattern =
    /import\s+\{\s*(\w+)\s*\}\s+from\s+"(\.\/[^"]+)\.js";/g;

  for (const match of indexSource.matchAll(importPattern)) {
    const registerFunction = match[1];
    const relativeImportPath = match[2];
    if (registerFunction && relativeImportPath) {
      importPathByRegisterFunction.set(
        registerFunction,
        resolve(mcpToolsRoot, `${relativeImportPath}.ts`),
      );
    }
  }

  return importPathByRegisterFunction;
}

function extractRegisterFunctionOrder(indexSource) {
  const registerFunctionOrder = [];
  const registerCallPattern = /^\s+(\w+)\(server\);$/gm;

  for (const match of indexSource.matchAll(registerCallPattern)) {
    const registerFunction = match[1];
    if (registerFunction) {
      registerFunctionOrder.push(registerFunction);
    }
  }

  return registerFunctionOrder;
}

function extractToolMetadata(toolSource, toolPath) {
  const nameMatch = toolSource.match(/server\.registerTool\(\s*"([^"]+)"/);
  const descriptionMatch = toolSource.match(/description:\s*"([^"]+)"/s);

  if (!(nameMatch?.[1] && descriptionMatch?.[1])) {
    throw new Error(`Unable to extract MCP tool metadata from ${toolPath}`);
  }

  return {
    name: nameMatch[1],
    description: normalizeWhitespace(descriptionMatch[1]),
  };
}

export function collectCanonicalMcpTools() {
  const indexSource = readFileSync(mcpToolsIndexPath, "utf8");
  const importPathByRegisterFunction =
    extractImportPathByRegisterFunction(indexSource);
  const registerFunctionOrder = extractRegisterFunctionOrder(indexSource);

  return registerFunctionOrder.map((registerFunction) => {
    const toolPath = importPathByRegisterFunction.get(registerFunction);
    if (!toolPath) {
      throw new Error(`Unable to resolve MCP tool file for ${registerFunction}`);
    }

    const toolSource = readFileSync(toolPath, "utf8");
    return extractToolMetadata(toolSource, toolPath);
  });
}

export function renderGeneratedMcpToolManifest(tools) {
  const entries = tools
    .map(
      (tool) =>
        `  {\n    name: ${JSON.stringify(tool.name)},\n    description:\n      ${JSON.stringify(tool.description)},\n  },`,
    )
    .join("\n");

  return `// Generated by scripts/sync-mcp-tool-manifest.js\nexport const MCP_TOOL_MANIFEST = [\n${entries}\n] as const;\n`;
}

export function syncGeneratedMcpToolManifest() {
  const output = renderGeneratedMcpToolManifest(collectCanonicalMcpTools());

  mkdirSync(dirname(generatedMcpToolManifestPath), { recursive: true });
  writeFileSync(generatedMcpToolManifestPath, output);

  return output;
}
